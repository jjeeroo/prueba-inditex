CREATE TABLE PRICES (
    PRICE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    BRAND_ID NUMBER(11) NOT NULL,
    START_DATE DATETIME NOT NULL,
    END_DATE DATETIME NOT NULL,
    PRODUCT_ID NUMBER NOT NULL,
    PRIORITY NUMBER NOT NULL,
    PRICE NUMBER(11,2) NOT NULL,
    CURR VARCHAR2(5) NOT NULL
);
